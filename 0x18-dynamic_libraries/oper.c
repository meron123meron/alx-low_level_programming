#include <stdio.h>

/**
 * _add - adds two numbers
 * @a: number 1
 * @b: number 2
 * _sub - subtracts two numbers
 * _mul - multiplicates two numbers
 * _div - divides two numbers
 * _mod - gives the remainder after division
 *
 * Return: integer value
 */
int add(int a, int b)
{
return ((a) + (b));
}
int sub(int a, int b)
{
return ((a) -(b));
}
int mul(int a, int b)
{
return ((a) * (b));
}
int div(int a, int b)
{
return (a / b);
}
int mod(int a, int b)
{
return (a % b);
}
